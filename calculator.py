#  Mini Calculator
first_value =int(input("enter your first number :"))
second_value =int(input("enter your second number :"))
choice =input("chose one of the following :'+,-,*,/")
if choice == "+":
       print(first_value + second_value)
elif choice == "-":
           print(first_value - second_value)
elif choice == "*" :
            print(first_value * second_value)
elif choice == "/":
            print(first_value / second_value)
else:
        print('invalid choice')

   
       







    